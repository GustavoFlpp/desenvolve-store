/**
 * Retorna o título traduzido do produto ou o original.
 */
export function getTranslatedTitle(id: number, originalTitle: string): string {
  return translations[id]?.title ?? originalTitle;
}

/**
 * Retorna a descrição traduzida do produto ou a original.
 */
export function getTranslatedDescription(id: number, originalDescription: string): string {
  return translations[id]?.description ?? originalDescription;
}

